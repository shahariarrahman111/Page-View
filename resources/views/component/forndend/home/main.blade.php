<script>
    // products Quantity incre and decre button

    $(document).on('click', '.add-cart-btn', function (e) {
        e.preventDefault();
        $(this).addClass('d-none');
        $(this).parents('div.shoppingBtn').find('div.qtyContainer').removeClass('d-none');
        $(this).parents('div.shoppingBtn').find('div.qtyContainer').addClass('d-flex');

    })

    function updateQty(qty, id) {
        let currentQty = parseInt($('#qty' + id).val());
        let newQty = currentQty + qty;
        if (newQty < 1 || newQty == NaN) {
            newQty = 0;
            $('#qty' + id).parents('div.qtyContainer').addClass('d-none');
            $('#qty' + id).parents('div.shoppingBtn').find('.add-cart-btn').removeClass('d-none');
            $('#qty' + id).parents('div.shoppingBtn').find('.add-cart-btn').addClass('d-block');
        }
        $('#qty' + id).val(newQty);
    }


    $(document).on('keyup', '.qty', function (e) {
        e.preventDefault();
        let newQty = parseInt($(this).val());
        if (newQty < 1 || newQty == " ") {
            $(this).parents('div.qtyContainer').addClass('d-none');
            $(this).parents('div.shoppingBtn').find('.add-cart-btn').removeClass('d-none');
            $(this).parents('div.shoppingBtn').find('.add-cart-btn').addClass('d-block');
        }
    })

</script>

